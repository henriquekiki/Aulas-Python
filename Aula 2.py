a = int(input('Primeiro valor:'))
b = int(input('Segundo valor:'))
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
print('Soma: {soma}.'
      '\nSubtração: {sub}.'
      '\nMultiplicação {multi}'
      '\nDivisão: {div}'
      '\nResto: {resto}'.format(soma=soma,
                                sub=subtracao,
                                multi=multiplicacao,
                                div=divisao,
                                resto=resto))
